package com.example.pizzagood.ui.home.Model;

public class Pizza {

    double black_Olives = 0;
    double tomatoes = 0;
    double Onion = 0;
    double Bacon = 0;
    double Chicken = 0;
    double peppers = 0;

    public Pizza() {
    }

    public double getBlack_Olives() {
        return black_Olives;
    }

    public double getTomatoes() {
        return tomatoes;
    }

    public double getOnion() {
        return Onion;
    }

    public double getBacon() {
        return Bacon;
    }

    public double getChicken() {
        return Chicken;
    }

    public double getPeppers() {
        return peppers;
    }
}
